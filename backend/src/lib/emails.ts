import { sendMail } from "@/lib/mailer"

export async function mailPaperReady(opts: {
  teacherEmail: string
  teacherName: string
  assignmentTitle: string
  subject: string
  assignmentId: string
  frontendUrl: string
}) {
  const url = `${opts.frontendUrl}/paper/${opts.assignmentId}`
  await sendMail({
    to: opts.teacherEmail,
    subject: `Your question paper is ready — ${opts.assignmentTitle}`,
    html: `
      <div style="font-family:sans-serif;max-width:520px;margin:0 auto;padding:32px 24px;background:#fff">
        <h2 style="margin:0 0 8px;font-size:20px;color:#111">Question paper ready ✓</h2>
        <p style="color:#555;margin:0 0 24px;font-size:15px">
          Hi ${opts.teacherName}, your paper for <strong>${opts.assignmentTitle}</strong>
          (${opts.subject}) has been generated by VedaAI.
        </p>
        <a href="${url}"
           style="display:inline-block;background:#e74c25;color:#fff;text-decoration:none;
                  padding:12px 24px;border-radius:8px;font-weight:600;font-size:14px">
          View Question Paper
        </a>
        <p style="color:#aaa;font-size:12px;margin-top:32px">
          VedaAI — AI-powered exam paper generator
        </p>
      </div>`,
  })
}

